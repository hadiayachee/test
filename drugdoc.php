In pharmacology, a drug is a chemical substance, typically of known structure, which, when administered to a living organism, produces a biological effect. A pharmaceutical drug, also called a medication or medicine, is a chemical substance used to treat, cure, prevent, or diagnose a disease or to promote well-being.[1] Traditionally drugs were obtained through extraction from medicinal plants, but more recently also by organic synthesis.Pharmaceutical drugs may be used for a limited duration, or on a regular basis for chronic disorders.

